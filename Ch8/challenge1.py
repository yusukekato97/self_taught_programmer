import statistics

num = [2, 14, 22, 51, 18, 14, 33, 89, 71, 2, 71, 14]

print(statistics.mean(num))
print(statistics.median(num))
print(statistics.median_low(num))
print(statistics.median_high(num))
print(statistics.mode(num))

print(statistics.pstdev(num))
print(statistics.pvariance(num))
